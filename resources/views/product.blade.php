    <script>
        // JavaScript to handle image switching
        $(document).ready(function () {
            $('.product__thumb__pic').on('click', function () {
                // Remove the 'active' class from all thumbnail links
                $('.nav-link').removeClass('active');
                
                // Add the 'active' class to the clicked thumbnail link
                $(this).closest('a').addClass('active');
                
                // Get the image source from the clicked thumbnail
                var imgSrc = $(this).css('background-image').replace('url("', '').replace('")', '');
                
                // Update the main image with the new source
                $('.product__details__pic__item img').attr('src', imgSrc);
            });
        });
